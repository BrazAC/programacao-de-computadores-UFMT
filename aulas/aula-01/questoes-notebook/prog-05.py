#Algoritmo
'''
Ex:
print(f"27.5 + 2 = {27.5 + 2}")
...
'''

def main():
    print(f"27.5 + 2 = {27.5 + 2}")
    print(f"27.5 - 2 = {27.5 - 2}")
    print(f"27.5 * 2 = {27.5 * 2}")
    print(f"27.5 / 2 = {27.5 / 2}")
    print(f"27.5 // 2 = {27.5 // 2}")
    print(f"27.5 ** 2 = {27.5 ** 2}")

if __name__ == "__main__":
    main()